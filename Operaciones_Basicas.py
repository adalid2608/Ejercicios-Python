print ("---Operaciones basicas---");

a = 12;
b = 6;

#Resultado de las operaciones
suma = a + b; #Suma de dos variables
resta = a - b; #Resta de dos variables
multiplicacion = a * b; #Multiplicación de dos variables
division = a / b; #División de dos variables
cociente = a // b; #Cociente de la división
modulo = a % b; #Residuo de la división
potencia = a ** b; #Elevacion de potencia

#Mensajes con el resultado de cada una de las operaciones
print("El resultado de la suma es: ",suma)
print("El resultado de la resta es: ",resta)
print("El resultado de la multiplicación es: ",multiplicacion)
print("El resultado de la división es: ",division)
print("El cociente de la división es: ",cociente)
print("El residuo de la división es: ",modulo)
print("El resultado de",a, "elevado a la potencia",b,"es", potencia)

